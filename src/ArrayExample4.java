public class ArrayExample4 {
    public static void main(String[] args) {
        int[] array = {1,2,3,4,5,6};
        String[] array2 = {"a","b", "c", "d", "e"};
        for(int a:array){
            System.out.println(a);
        }
        for(String b:array2){
            System.out.println(b);
        }
    }
}
